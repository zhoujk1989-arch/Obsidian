/*
业务目标：
- 校验 PROC_EAST_IE_001_104 执行后 IE_001_104 数据质量。

目标系统：
- EAST5.0系统。

目标产物：
- 校验 SQL。

依赖知识页：
- [[数据表-IE_001_104-岗位信息表-EAST5.0系统]]
- [[来源-EAST5.0系统-IE_001_104-岗位信息表]]

参数：
- I_DATE：采集日期，格式 YYYYMMDD。
*/

-- ============================================================
-- 1. 行数检查：目标表当期数据量
-- ============================================================
SELECT
    COUNT(*) AS target_row_count
FROM IE_001_104
WHERE CJRQ = '20260430';  -- 替换为实际采集日期

-- ============================================================
-- 2. 主键重复检查：(NBJGH, CJRQ, GWBH) 是否有重复
-- ============================================================
SELECT
    NBJGH,
    CJRQ,
    GWBH,
    COUNT(*) AS cnt
FROM IE_001_104
WHERE CJRQ = '20260430'  -- 替换为实际采集日期
GROUP BY NBJGH, CJRQ, GWBH
HAVING COUNT(*) > 1;

-- ============================================================
-- 3. 必填字段为空检查：内部机构号(NBJGH)、岗位编号(GWBH)、采集日期(CJRQ)
-- ============================================================
SELECT
    COUNT(*) AS null_nbjgh_count
FROM IE_001_104
WHERE CJRQ = '20260430'  -- 替换为实际采集日期
  AND (NBJGH IS NULL OR TRIM(NBJGH) = '');

SELECT
    COUNT(*) AS null_gwbh_count
FROM IE_001_104
WHERE CJRQ = '20260430'  -- 替换为实际采集日期
  AND (GWBH IS NULL OR TRIM(GWBH) = '');

-- ============================================================
-- 4. 采集日期格式检查：CJRQ 是否全为 8 位 YYYYMMDD 格式
-- ============================================================
SELECT
    CJRQ,
    COUNT(*) AS cnt
FROM IE_001_104
WHERE CJRQ = '20260430'  -- 替换为实际采集日期
GROUP BY CJRQ
HAVING COUNT(*) > 0;

-- ============================================================
-- 5. 内部机构号格式检查：截取后的 NBJGH 是否非空
-- ============================================================
SELECT
    COUNT(*) AS null_internal_org_count
FROM IE_001_104
WHERE CJRQ = '20260430'  -- 替换为实际采集日期
  AND (NBJGH IS NULL OR CHAR_LENGTH(NBJGH) = 0);

-- ============================================================
-- 6. 来源追溯检查：抽样验证源表与目标表数据一致性
-- ============================================================
-- 检查源表 T_1_4 当期数据量（含撤销当月和 9999-12-31）
SELECT
    COUNT(*) AS source_row_count
FROM T_1_4
WHERE A040010 = DATE_FORMAT(STR_TO_DATE('20260430', '%Y%m%d'), '%Y-%m-%d')
   OR A040010 = '9999-12-31';  -- 替换为实际采集日期

-- 检查目标表金融许可证号映射覆盖率
SELECT
    COUNT(*) AS null_jrxkzh_count
FROM IE_001_104
WHERE CJRQ = '20260430'  -- 替换为实际采集日期
  AND (JRXKZH IS NULL OR TRIM(JRXKZH) = '');

-- ============================================================
-- 7. 去重逻辑验证：同一内部机构号+岗位编号是否唯一
-- ============================================================
SELECT
    NBJGH,
    GWBH,
    COUNT(*) AS cnt
FROM IE_001_104
WHERE CJRQ = '20260430'  -- 替换为实际采集日期
GROUP BY NBJGH, GWBH
HAVING COUNT(*) > 1;

-- ============================================================
-- 8. 岗位撤销日期过滤验证：目标表中不应存在未来撤销日期
-- ============================================================
-- 注意：本校验依赖源表 T_1_4 的 A040010 字段
-- 目标表 IE_001_104 不保留 A040010 字段，因此通过行数对比间接验证
SELECT
    'source' AS source_table,
    COUNT(*) AS row_count
FROM T_1_4
WHERE A040010 = DATE_FORMAT(STR_TO_DATE('20260430', '%Y%m%d'), '%Y-%m-%d')
   OR A040010 = '9999-12-31'
UNION ALL
SELECT
    'target' AS source_table,
    COUNT(*) AS row_count
FROM IE_001_104
WHERE CJRQ = '20260430';  -- 替换为实际采集日期
